export function gematriaChapter(n: number): string {
  if (n <= 10) return NUMERALS[n];
  if (n < 20) return 'י' + NUMERALS[n - 10];
  if (n === 20) return 'כ';
  if (n < 30) return 'כ' + NUMERALS[n - 20];
  if (n === 30) return 'ל';
  if (n < 40) return 'ל' + NUMERALS[n - 30];
  if (n === 40) return 'מ';
  if (n < 50) return 'מ' + NUMERALS[n - 40];
  if (n === 50) return 'נ';
  if (n < 60) return 'נ' + NUMERALS[n - 50];
  if (n === 60) return 'ס';
  if (n < 70) return 'ס' + NUMERALS[n - 60];
  if (n === 70) return 'ע';
  if (n < 80) return 'ע' + NUMERALS[n - 70];
  if (n === 80) return 'פ';
  if (n < 90) return 'פ' + NUMERALS[n - 80];
  if (n === 90) return 'צ';
  if (n < 100) return 'צ' + NUMERALS[n - 90];
  if (n === 100) return 'ק';
  if (n < 110) return 'ק' + NUMERALS[n - 100];
  if (n === 110) return 'קי';
  if (n < 115) return 'קי' + NUMERALS[n - 110];
  if (n === 115) return 'קטו';
  if (n === 116) return 'קטז';
  if (n < 120) return 'קי' + NUMERALS[n - 110];
  if (n === 120) return 'קכ';
  if (n < 130) return 'קכ' + NUMERALS[n - 120];
  if (n === 130) return 'קל';
  if (n < 140) return 'קל' + NUMERALS[n - 130];
  if (n === 140) return 'קמ';
  if (n < 150) return 'קמ' + NUMERALS[n - 140];
  if (n === 150) return 'קנ';
  return String(n);
}

