const MONTH_NAMES: Record<number, string> = {
  [months.TISHREI]: 'tishrei',
  [months.CHESHVAN]: 'cheshvan',
  [months.KISLEV]: 'kislev',
  [months.TEVET]: 'tevet',
  [months.SHVAT]: 'shvat',
  [months.ADAR_I]: 'adar-i',
  [months.ADAR_II]: 'adar-ii',
  [months.NISAN]: 'nisan',
  [months.IYYAR]: 'iyar',
  [months.SIVAN]: 'sivan',
  [months.TAMUZ]: 'tammuz',
  [months.AV]: 'av',
  [months.ELUL]: 'elul',
};

/** Returns history events for the given Gregorian date based on Hebrew calendar. */
export function historyForDate(date: Date = new Date()): HistoryEvent[] {
  const hd = new HDate(date);
  const m = hd.getMonth();
  const d = hd.getDate();
  const monthName = MONTH_NAMES[m];
  if (!monthName) return [];
  const key = `${monthName}-${d}`;
  // In a non-leap year, prefer "adar-..." over "adar-i" if both exist.
  let events = HISTORY[key] ?? [];
  if (m === months.ADAR_I && !HDate.isLeapYear(hd.getFullYear())) {
    events = (HISTORY[`adar-${d}`] ?? []).concat(events);
  }
  return events;
}
